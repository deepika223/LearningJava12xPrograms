package Test1;

public class Comparision_Logical {
    public static void main(String[] args) {

        int a=10, b=20;
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);
        System.out.println(a==b);
        System.out.println((a>b)&&(a>0));
        System.out.println((a<b)||(a>0));
        System.out.println(!(a>b));
    }
}
