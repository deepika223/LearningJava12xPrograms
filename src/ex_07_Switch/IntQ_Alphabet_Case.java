package ex_07_Switch;

public class IntQ_Alphabet_Case {
    public static void main(String[] args) {
        char code = 'D';
        switch (code){
            default:
                System.out.println("Hellooooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }

    }
}
