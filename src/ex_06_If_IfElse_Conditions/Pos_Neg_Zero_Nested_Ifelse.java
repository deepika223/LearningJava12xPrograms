package ex_06_If_IfElse_Conditions;

public class Pos_Neg_Zero_Nested_Ifelse {
    public static void main(String[] args) {
        int num=3;

        if(num>0)
        {
            System.out.println("Positive");
        }

        else if(num<0)
        {
            System.out.println("Negative");
        }

        else
        {
            System.out.println("Zero");
        }
    }
}
